#include "libft.h"

int		atoi_whitespace(void)
{
	if (ft_atoi("   2") == 2 && ft_atoi("9   9") == 9
			&& ft_atoi("   -22") == -22 && ft_atoi("    -329") == -329)
		return (0);
	else
		return (-1);
}
